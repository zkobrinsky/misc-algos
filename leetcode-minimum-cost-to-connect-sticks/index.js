function countSticks(sticks) {
  let min = Infinity;
  let secondMin = Infinity;
  let minIndex = 0;
  let secondIndex = 0;
  let cost = 0;

  return recurse(sticks);
  function recurse(sticks) {
    min = Infinity;
    secondMin = Infinity;
    let innerCost = 0;
    if (sticks.length < 2) {
      return cost;
    }

    for (let i = 0; i < sticks.length; i++) {
      if (sticks[i] < min) {
        min = sticks[i];
        minIndex = i;
      }
    }
    innerCost = min;
    sticks.splice(minIndex, 1);

    for (let i = 0; i < sticks.length; i++) {
      if (sticks[i] < secondMin) {
        secondMin = sticks[i];
        secondIndex = i;
      }
    }

    innerCost += secondMin;
    sticks[secondIndex] = innerCost;

    cost += innerCost;
    return recurse(sticks);
  }
}

